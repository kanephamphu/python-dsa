# 3. Minimum Cost Climbing Stairs
# Problem
# You are given an integer array cost where cost[i] is the cost of step i.
# You can start from step 0 or step 1.
# Once you pay the cost, you can climb:
# 1 step
# 2 steps
# Return the minimum cost to reach the top of the floor.
# Example
# Input
# cost = [10,15,20]
# Output
# 15
# Explanation
# Start at step 1 → pay 15 → reach the top.
# Constraints
# 2 ≤ cost.length ≤ 1000
# 0 ≤ cost[i] ≤ 999 